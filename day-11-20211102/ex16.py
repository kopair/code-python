def add(a, b):
  print(f"{a} & {b}")
  return a + b

def subtract(a, b):
  print(f"{a} - {b}")
  return a - b

def multiply(a, b):
  print(f"{a} * {b}")
  return a * b

def divide(a, b):
  print(f"{a} / {b}")
  return a / b

age = add(10,20)
sub = subtract(190,4)
asd = multiply(3,19)
cds = divide(3000,20)

print(f"{age} , {sub} , {asd} , {cds}")